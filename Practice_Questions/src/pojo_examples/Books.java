package pojo_examples;

public class Books {
	
	String bookName;
	String author;
	int isbn;
	int year;
	public String getBookName() {
		return bookName;
	}
	public void setBookName(String bookName) {
		this.bookName = bookName;
	}
	public String getAuthor() {
		return author;
	}
	public void setAuthor(String author) {
		this.author = author;
	}
	public int getIsbn() {
		return isbn;
	}
	public void setIsbn(int isbn) {
		this.isbn = isbn;
	}
	public int getYear() {
		return year;
	}
	public void setYear(int year) {
		this.year = year;
	}
	@Override
	public String toString() {
		return "Books [bookName=" + bookName + ", author=" + author + ", isbn=" + isbn + ", year=" + year + "]";
	}
	
	

}
